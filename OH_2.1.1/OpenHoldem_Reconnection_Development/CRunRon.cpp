void CRunRon::GetCounts(void)
{
	unsigned int	ocard[2] = {0};
	int				index1 = 0, index2 = 0;

	int				i = 0;
	CardMask		DeadCards, ExtraCards, EvalCards, ComCards, AllDeadCards;
	int				nDeadCards = 0, nComCards = 0;
	HandVal			hv = 0;
	int				hv_totals[10] = {0};
	double			dummy = 0.;
	double			max_pokval = 0., max_pokval_count = 0;
	unsigned int	pokval = 0;
	LARGE_INTEGER	bcount = {0}, ecount = {0};

	int sym_br = (int) p_symbols->sym()->br;
	int sym_userchair = (int) p_symbols->sym()->userchair;

	unsigned int	pcard[2], ccard[5];
	for (i=0; i<=1; i++)
		pcard[i] = p_scraper->card_player(sym_userchair, i);
	for (i=0; i<=4; i++)
		ccard[i] = p_scraper->card_common(i);

	// Get the lock
	CSLock lock(m_critsec);


	// Preflop is pre-calculated
	// Also use the pre-flop code if there is a common card animation going on, to prevent freezeups
	if (sym_br==1 || p_scraper->IsCommonAnimation())
	{

		if (!p_symbols->user_chair_confirmed())
			return;

		//////////////////////////////////////////////////////////////
		// run symbols

		// time it
		QueryPerformanceCounter(&bcount);

		_srun.royfl = _srun.strfl = _srun.fourkind = _srun.fullh = _srun.flush = _srun.strai = _srun.threekind = 0.0;
		_srun.twopair = _srun.onepair = _srun.hcard = _srun.total = _srun.pokervalmax = _srun.prnuts = _srun.prbest = _srun.clocks = 0.0;

		// suited, store in hi/lo
		if (StdDeck_SUIT(pcard[0]) == StdDeck_SUIT(pcard[1]))
		{
			if (StdDeck_RANK(pcard[0]) >= StdDeck_RANK(pcard[1]))
			{
				index1 = StdDeck_RANK(pcard[0]);
				index2 = StdDeck_RANK(pcard[1]);
			}
			else
			{
				index1 = StdDeck_RANK(pcard[1]);
				index2 = StdDeck_RANK(pcard[0]);
			}
		}
		// unsuited, store in lo/hi
		else {
			if (StdDeck_RANK(pcard[0]) >= StdDeck_RANK(pcard[1]))
			{
				index1 = StdDeck_RANK(pcard[1]);
				index2 = StdDeck_RANK(pcard[0]);
			}
			else
			{
				index1 = StdDeck_RANK(pcard[0]);
				index2 = StdDeck_RANK(pcard[1]);
			}
		}

		// time it
		QueryPerformanceCounter(&ecount);

		// populate symbols
		_srun.royfl = _run_count[index1][index2][9];
		_srun.strfl = _run_count[index1][index2][8];
		_srun.fourkind = _run_count[index1][index2][7];
		_srun.fullh = _run_count[index1][index2][6];
		_srun.flush = _run_count[index1][index2][5];
		_srun.strai = _run_count[index1][index2][4];
		_srun.threekind = _run_count[index1][index2][3];
		_srun.twopair = _run_count[index1][index2][2];
		_srun.onepair = _run_count[index1][index2][1];
		_srun.hcard = _run_count[index1][index2][0];
		_srun.total = _srun.royfl+_srun.strfl+_srun.fourkind+_srun.fullh+_srun.flush+
					_srun.strai+_srun.threekind+_srun.twopair+_srun.onepair+_srun.hcard;
		_srun.prbest = _run_max_pokval_n[index1][index2]/_srun.total;
		_srun.pokervalmax = _run_max_pokval[index1][index2];
		_srun.pokervalmaxcount = _run_max_pokval_n[index1][index2];
		_srun.clocks = ecount.LowPart - bcount.LowPart;


		//////////////////////////////////////////////////////////////
		// ron symbols
		// time it
		QueryPerformanceCounter(&bcount);

		_sron.royfl = _sron.strfl = _sron.fourkind = _sron.fullh = _sron.flush = _sron.strai = _sron.threekind = 0.0;
		_sron.twopair = _sron.onepair = _sron.hcard = _sron.total = _sron.pokervalmax = _sron.prnuts = _sron.prbest = _sron.clocks = 0.0;

		// suited, store in hi/lo
		if (StdDeck_SUIT(pcard[0]) == StdDeck_SUIT(pcard[1]))
		{
			if (StdDeck_RANK(pcard[0]) >= StdDeck_RANK(pcard[1]))
			{
				index1 = StdDeck_RANK(pcard[0]);
				index2 = StdDeck_RANK(pcard[1]);
			}
			else
			{
				index1 = StdDeck_RANK(pcard[1]);
				index2 = StdDeck_RANK(pcard[0]);
			}
		}

		// unsuited, store in lo/hi
		else
		{
			if (StdDeck_RANK(pcard[0]) >= StdDeck_RANK(pcard[1]))
			{
				index1 = StdDeck_RANK(pcard[1]);
				index2 = StdDeck_RANK(pcard[0]);
			}
			else
			{
				index1 = StdDeck_RANK(pcard[0]);
				index2 = StdDeck_RANK(pcard[1]);
			}
		}

		// time it
		QueryPerformanceCounter(&ecount);

		// populate symbols
		_sron.royfl = _ron_count[index1][index2][9];
		_sron.strfl = _ron_count[index1][index2][8];
		_sron.fourkind = _ron_count[index1][index2][7];
		_sron.fullh = _ron_count[index1][index2][6];
		_sron.flush = _ron_count[index1][index2][5];
		_sron.strai = _ron_count[index1][index2][4];
		_sron.threekind = _ron_count[index1][index2][3];
		_sron.twopair = _ron_count[index1][index2][2];
		_sron.onepair = _ron_count[index1][index2][1];
		_sron.hcard = _ron_count[index1][index2][0];
		_sron.total = _sron.royfl+_sron.strfl+_sron.fourkind+_sron.fullh+_sron.flush+
					_sron.strai+_sron.threekind+_sron.twopair+_sron.onepair+_sron.hcard;
		_sron.prbest = _ron_max_pokval_n[index1][index2]/_sron.total;
		_sron.pokervalmax = _ron_max_pokval[index1][index2];
		_sron.pokervalmaxcount = _ron_max_pokval_n[index1][index2];

		_sron.clocks = ecount.LowPart - bcount.LowPart;

	}


	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


	// Post flop is calculated on the fly
	else if (sym_br>=2)
	{
		//////////////////////////////////////////////////////////////
		// run symbols

		// time it
		QueryPerformanceCounter(&bcount);

		// player cards
		nDeadCards = nComCards = 0;
		CardMask_RESET(DeadCards);
		for (i=0; i<=1; i++)
		{
			if (pcard[i] != CARD_BACK && pcard[i] != CARD_NOCARD)
			{
				CardMask_SET(DeadCards, pcard[i]);
				nDeadCards++;
			}
		}

		// common cards
		for (i=0; i<=4; i++)
		{
			if (ccard[i] != CARD_BACK && ccard[i] != CARD_NOCARD)
			{
				CardMask_SET(DeadCards, ccard[i]);
				nDeadCards++;
				nComCards++;
			}
		}

		// setup the counters
		for (i=0; i<=9; i++)
			hv_totals[i] = 0;

		max_pokval = max_pokval_count = 0;

		// Do the enumeration
		ENUMERATE_N_CARDS_D(ExtraCards, 5-nComCards, DeadCards,
		{
			CardMask_OR(EvalCards, DeadCards, ExtraCards);
			hv = Hand_EVAL_N(EvalCards, 7);
			pokval = p_symbols->CalcPokerval(hv, 7, &dummy, CARD_NOCARD, CARD_NOCARD);

			if (pokval>max_pokval) {
				max_pokval = pokval;
				max_pokval_count=1;
			}
			else if (pokval==max_pokval) {
				max_pokval_count++;
			}

			if (HandVal_HANDTYPE(hv)==HandType_STFLUSH && StdDeck_RANK(HandVal_TOP_CARD(hv))==Rank_ACE) {
				++hv_totals[9];
			}
			else {
				++hv_totals[HandVal_HANDTYPE(hv)];
			}
		});

		// time it
		QueryPerformanceCounter(&ecount);

		// populate symbols
		_srun.royfl = hv_totals[9];
		_srun.strfl = hv_totals[8];
		_srun.fourkind = hv_totals[7];
		_srun.fullh = hv_totals[6];
		_srun.flush = hv_totals[5];
		_srun.strai = hv_totals[4];
		_srun.threekind = hv_totals[3];
		_srun.twopair = hv_totals[2];
		_srun.onepair = hv_totals[1];
		_srun.hcard = hv_totals[0];
		_srun.total = _srun.royfl+_srun.strfl+_srun.fourkind+_srun.fullh+_srun.flush+
					_srun.strai+_srun.threekind+_srun.twopair+_srun.onepair+_srun.hcard;
		_srun.prbest = max_pokval_count/_srun.total;
		_srun.pokervalmax = max_pokval;
		_srun.pokervalmaxcount = max_pokval_count;
		_srun.clocks = ecount.LowPart - bcount.LowPart;

		//////////////////////////////////////////////////////////////
		// ron symbols

		// time it
		QueryPerformanceCounter(&bcount);

		// player cards
		nDeadCards = nComCards = 0;
		CardMask_RESET(DeadCards);
		for (i=0; i<=1; i++)
		{
			if (pcard[i] != CARD_BACK && pcard[i] != CARD_NOCARD)
			{
				CardMask_SET(DeadCards, pcard[i]);
				nDeadCards++;
			}
		}

		// common cards
		CardMask_RESET(ComCards);
		for (i=0; i<=4; i++)
		{
			if (ccard[i] != CARD_BACK && ccard[i] != CARD_NOCARD)
			{
				CardMask_SET(DeadCards, ccard[i]);
				CardMask_SET(ComCards, ccard[i]);
				nDeadCards++;
				nComCards++;
			}
		}

		// setup the counters
		for (i=0; i<=9; i++)
			hv_totals[i] = 0;

		max_pokval = max_pokval_count = 0;

		// Enumerate through all possible opponent hands (excludes player and common cards)
		for (ocard[0]=0; ocard[0]<=50; ocard[0]++)
		{
			for (ocard[1]=ocard[0]+1; ocard[1]<=51; ocard[1]++)
			{
				if (!CardMask_CARD_IS_SET(DeadCards, ocard[0]) &&
						!CardMask_CARD_IS_SET(DeadCards, ocard[1]))
				{

					// All dead cards (includes opp cards too)
					AllDeadCards = DeadCards;
					CardMask_SET(AllDeadCards, ocard[0]);
					CardMask_SET(AllDeadCards, ocard[1]);

					ENUMERATE_N_CARDS_D(ExtraCards, 5-nComCards, AllDeadCards,
					{
						CardMask_OR(EvalCards, ComCards, ExtraCards);
						CardMask_SET(EvalCards, ocard[0]);
						CardMask_SET(EvalCards, ocard[1]);
						hv = Hand_EVAL_N(EvalCards, 7);
						pokval = p_symbols->CalcPokerval(hv, 7, &dummy, CARD_NOCARD, CARD_NOCARD);

						if (pokval>max_pokval) {
							max_pokval = pokval;
							max_pokval_count=1;
						}
						else if (pokval==max_pokval) {
							max_pokval_count++;
						}

						if (HandVal_HANDTYPE(hv)==HandType_STFLUSH && StdDeck_RANK(HandVal_TOP_CARD(hv))==Rank_ACE) {
							++hv_totals[9];
						}
						else {
							++hv_totals[HandVal_HANDTYPE(hv)];
						}
					});
				}
			}
		}

		// time it
		QueryPerformanceCounter(&ecount);

		// populate symbols
		_sron.royfl = hv_totals[9];
		_sron.strfl = hv_totals[8];
		_sron.fourkind = hv_totals[7];
		_sron.fullh = hv_totals[6];
		_sron.flush = hv_totals[5];
		_sron.strai = hv_totals[4];
		_sron.threekind = hv_totals[3];
		_sron.twopair = hv_totals[2];
		_sron.onepair = hv_totals[1];
		_sron.hcard = hv_totals[0];
		_sron.total = _sron.royfl+_sron.strfl+_sron.fourkind+_sron.fullh+_sron.flush+
					_sron.strai+_sron.threekind+_sron.twopair+_sron.onepair+_sron.hcard;
		_sron.prbest = max_pokval_count/_sron.total;
		_sron.pokervalmax = max_pokval;
		_sron.pokervalmaxcount = max_pokval_count;
		_sron.clocks = ecount.LowPart - bcount.LowPart;

	}

	// NUTS probabilities
	_srun.prnuts = 0;
	if (_srun.royfl>_sron.royfl)		_srun.prnuts+=(_srun.royfl-_sron.royfl);
	else if (_srun.strfl>_sron.strfl)	_srun.prnuts+=(_srun.strfl-_sron.strfl);
	else if (_srun.fourkind>_sron.fourkind)	_srun.prnuts+=(_srun.fourkind-_sron.fourkind);
	else if (_srun.fullh>_sron.fullh)	_srun.prnuts+=(_srun.fullh-_sron.fullh);
	else if (_srun.flush>_sron.flush)	_srun.prnuts+=(_srun.flush-_sron.flush);
	else if (_srun.strai>_sron.strai)	_srun.prnuts+=(_srun.strai-_sron.strai);
	else if (_srun.threekind>_sron.threekind)	_srun.prnuts+=(_srun.threekind-_sron.threekind);
	else if (_srun.twopair>_sron.twopair)	_srun.prnuts+=(_srun.twopair-_sron.twopair);
	else if (_srun.onepair>_sron.onepair)	_srun.prnuts+=(_srun.onepair-_sron.onepair);
	else if (_srun.hcard>_sron.hcard)	_srun.prnuts+=(_srun.hcard-_sron.hcard);
	_srun.prnuts = _srun.prnuts / _srun.total;

	_sron.prnuts = 0;
	if (_sron.royfl>_srun.royfl)		_sron.prnuts+=(_sron.royfl-_srun.royfl);
	else if (_sron.strfl>_srun.strfl)	_sron.prnuts+=(_sron.strfl-_srun.strfl);
	else if (_sron.fourkind>_srun.fourkind)	_sron.prnuts+=(_sron.fourkind-_srun.fourkind);
	else if (_sron.fullh>_srun.fullh)	_sron.prnuts+=(_sron.fullh-_srun.fullh);
	else if (_sron.flush>_srun.flush)	_sron.prnuts+=(_sron.flush-_srun.flush);
	else if (_sron.strai>_srun.strai)	_sron.prnuts+=(_sron.strai-_srun.strai);
	else if (_sron.threekind>_srun.threekind)	_sron.prnuts+=(_sron.threekind-_srun.threekind);
	else if (_sron.twopair>_srun.twopair)	_sron.prnuts+=(_sron.twopair-_srun.twopair);
	else if (_sron.onepair>_srun.onepair)	_sron.prnuts+=(_sron.onepair-_srun.onepair);
	else if (_sron.hcard>_srun.hcard)	_sron.prnuts+=(_sron.hcard-_srun.hcard);
	_sron.prnuts = _sron.prnuts / _sron.total;
}